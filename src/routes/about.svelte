        <h2>
            How my worldview developed.
        </h2>
        <p>
            I was 17 years old and was visiting Bangkok with my sisters. We went to a bookstore, and I picked up a book called "12 Rules for Life." This book in particular caught my attention because I had learnt a few things about about the book's author, <a href="https://www.jordanbpeterson.com/about/">Dr. Jordan Peterson</a>. I had learnt about Dr. Peterson from his interview with English news channel, Channel 4 News, in the <a href="https://youtu.be/aMcjxSThD54">video</a> titled "Jordan Peterson debate on the gender pay gap, campus protests and postmodernism." and thought that it would be an interesting read, since my fresh young mind was becoming increasingly interested in what the clinical psychology professor had to say.
        </p>        
        <p>
            I knew little about the West, its culture, its emphasis on individuality, and how in the modern age they had become a role model for other societies to follow—even after taking into considering their shortcomings. Reading the book and listening to Dr. Peterson's <a href="https://youtube.com/playlist?list=PL22J3VaeABQD_IZs7y60I3lUrrFTzkpat">Psychological Significance of the Biblical Stories</a> allowed me to have a better understanding of the West, a more concrete understanding of human psychology, motivation, and personality—at least as best an understanding my English comprehension could allow me to.
        </p>
